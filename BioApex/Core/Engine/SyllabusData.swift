    /// 按模块→章 分组（保持出现顺序）。
    static func chapters(in module: BioModule) -> [(chapter: String, points: [KnowledgePoint])] {
        var order: [String] = []
        var map: [String: [KnowledgePoint]] = [:]
        for p in points(in: module) {
            if map[p.chapter] == nil { order.append(p.chapter) }
            map[p.chapter, default: []].append(p)
        }
        return order.map { ($0, map[$0]!) }
    }

